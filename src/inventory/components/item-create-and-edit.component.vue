<script>
import CreateAndEdit from "../../shared/components/create-and-edit.component.vue";

export default {
  name: "item-create-and-edit-dialog",
  components: {CreateAndEdit},
  props: {
    item: null,
    visible: Boolean
  },
  data() {
    return {
      submitted: false
    }
  },
  methods: {
    onCancelRequested() {
      this.$emit('cancel-requested');
    },
    onSaveRequested() {
      this.submitted = true;
    }
  }
}
</script>

<template>
  <create-and-edit
      :entity="item"
      :visible="visible"
      entity-name="Item"
      @cancel-requested="onCancelRequested"
      @save-requested="onSaveRequested">
    <template #content>
      <div class="p-fluid">
        <div class="field mt-5">
          <pv-float-label>
            <label for="name">Name</label>
            <pv-input-text id="name" v-model="item.name"
                           :class="{'p-invalid': submitted && !item.name }"/>
          </pv-float-label>
        </div>

        <div class="field mt-5">
        <pv-float-label>
          <label for="quantity">Quantity</label>
          <pv-input-number id="quantity" v-model="item.quantity"
                           :class="{'p-invalid': submitted && !item.quantity }"/>
        </pv-float-label>
        </div>
        <div class="field mt-5">
          <pv-float-label >
            <label for="price">Price</label>
            <pv-input-text id="price" v-model="item.price"
                           :class="{'p-invalid': submitted && !item.price }"/>
          </pv-float-label>


        </div>
        <div class="field mt-5">
          <pv-float-label >
            <label for="imageUrl">Image URL</label>
            <pv-input-text id="imageUrl" v-model="item.imageUrl"
                           :class="{'p-invalid': submitted && !item.imageUrl }"/>
          </pv-float-label>


        </div>
      </div>
    </template>
  </create-and-edit>
</template>

